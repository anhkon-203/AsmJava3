    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int row = tblDiem.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Hãy chọn vào 1 dòng trên table");
            return;
        }
        String maSV = txtMaSv.getText();
        int confirm = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn xoá? ");

        if (confirm == JOptionPane.NO_OPTION) {
            return;
        } else if (confirm == JOptionPane.CANCEL_OPTION) {
            return;
        }
        if (confirm == JOptionPane.YES_OPTION) {
            lstServiceImpl.delete(maSV);
            JOptionPane.showMessageDialog(this, "Xoá thành công");
            loadData(lstServiceImpl.getlstTop3());
            this.clearForm();
            return;
        } else {
            JOptionPane.showMessageDialog(this, "Xoá thất bại");
        }

    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        Diem d = new Diem();
        String maSV = txtMaSv.getText();
        String tiengAnh = txtTiengAnh.getText();
        String tinHoc = txtTinHoc.getText();
        String gdtc = txtGDTC.getText();
        float avg = d.getDiemTB();
//        lblDiemTB.setText(String.valueOf(avg));

        if (tiengAnh.isEmpty() || tinHoc.isEmpty() || gdtc.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không được để trống");
            return;
        }
        try {
            double TA = Double.parseDouble(txtTiengAnh.getText());
            double TinHoc = Double.parseDouble(txtTinHoc.getText());
            double GDTC = Double.parseDouble(txtGDTC.getText());
            if (TA < 0 || TA > 10) {
                JOptionPane.showMessageDialog(this, "Điểm phải lớn hơn 0 và nhỏ hơn 10");
                return;
            }
            if (TinHoc < 0 || TinHoc > 10) {
                JOptionPane.showMessageDialog(this, "Điểm phải lớn hơn 0 và nhỏ hơn 10");
                return;
            }
            if (GDTC < 0 || GDTC > 10) {
                JOptionPane.showMessageDialog(this, "Điểm phải lớn hơn 0 và nhỏ hơn 10");
                return;
            }

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Điểm phải là số");
        }
        d.setMaSV(maSV);
        d.setTiengAnh(Float.parseFloat(tiengAnh));
        d.setTinHoc(Float.parseFloat(tinHoc));
        d.setGDTC(Float.parseFloat(gdtc));
        lblDiemTB.setText(String.valueOf(d.getDiemTB()));
        if (lstServiceImpl.Update(maSV, d)) {
            JOptionPane.showMessageDialog(this, "Update thành công");
            loadData(lstServiceImpl.getlstTop3());
        } else {
            JOptionPane.showMessageDialog(this, "Update thất bại");
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

