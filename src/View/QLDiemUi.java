    public boolean Find(String maSv) {
        String sql = "SELECT  dbo.SinhVien.maSv,dbo.SinhVien.ho_ten,tieng_anh,tin_hoc,gdtc,(tieng_anh+tin_hoc+gdtc) / 3 AS AVG\n"
                + "	FROM dbo.Diem JOIN dbo.SinhVien ON SinhVien.maSv = Diem.maSv\n"
                + "	WHERE SinhVien.maSv = ?";
        try ( Connection con = connection.getConnection();  PreparedStatement ps = con.prepareStatement(sql)) {
            ps.setString(1, maSv);
            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
                  txtMaSv.setText(rs.getString(1));
                  lblTen.setText(rs.getString(2));
                  txtTiengAnh.setText(String.valueOf(rs.getFloat(3)));
                  txtTinHoc.setText(String.valueOf(rs.getFloat(4)));
                  txtGDTC.setText(String.valueOf(rs.getFloat(5)));
                  lblDiemTB.setText(String.valueOf(rs.getFloat(6)));
                return true;
            }
        } catch (Exception e) {
        }
        return false;
    }

    public boolean selectItem() {
        String sql = "SELECT * FROM dbo.SinhVien\n"
                + "	WHERE maSv = ? ";
        try ( Connection con = connection.getConnection();  PreparedStatement ps = con.prepareStatement(sql)) {
            String ma = (String) cbbMaSV.getSelectedItem();
            ps.setString(1, ma);
            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
                txtMaSv.setText(ma);
                lblTen.setText(rs.getString("ho_ten"));
            }
        } catch (Exception e) {
            System.out.println(e);
        }
        return false;
    }
