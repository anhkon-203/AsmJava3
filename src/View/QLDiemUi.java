    private void loadData(ArrayList<Diem> list) {
        dtm = (DefaultTableModel) tblDiem.getModel();
        dtm.setRowCount(0);
        for (Diem d : list) {
            dtm.addRow(new Object[]{
                d.getMaSV(),
                d.getTen_sv(),
                d.getTiengAnh(),
                d.getTinHoc(),
                d.getGDTC(),
                d.getDiemTB(),});
        }
    }

